package kr.inbody.vo;

public class InbodyVO {
	private int inb_num;
	private int mem_num;
	private int inb_hei;
	private int inb_wei;
	private int inb_mus;
	private String inb_date;
	private String inb_photo;
	
	public int getInb_num() {
		return inb_num;
	}
	public void setInb_num(int inb_num) {
		this.inb_num = inb_num;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getInb_hei() {
		return inb_hei;
	}
	public void setInb_hei(int inb_hei) {
		this.inb_hei = inb_hei;
	}
	public int getInb_wei() {
		return inb_wei;
	}
	public void setInb_wei(int inb_wei) {
		this.inb_wei = inb_wei;
	}
	public int getInb_mus() {
		return inb_mus;
	}
	public void setInb_mus(int inb_mus) {
		this.inb_mus = inb_mus;
	}
	public String getInb_date() {
		return inb_date;
	}
	public void setInb_date(String inb_date) {
		this.inb_date = inb_date;
	}
	public String getInb_photo() {
		return inb_photo;
	}
	public void setInb_photo(String inb_photo) {
		this.inb_photo = inb_photo;
	}
}
